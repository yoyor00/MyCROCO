             write(file_saveflx_id, "(A, I4,A,I4,A,I4,A)") "call eco3m_write_fluxoutputs(ss_flux,", &
            ili, ", ",jcol, ", ", nbproc_loc, ")"
             write(file_saveflx_id, *) 
             write(file_saveflx_id,*) 'deallocate (ss_flux)'
             write(file_saveflx_id, *) "! =========================================="
           endif
           write(file_saveflx_id, *) 
           write(file_saveflx_id,*) ''
           ! update of the global index
           idproc_mat = idproc_mat + 1
    enddo
