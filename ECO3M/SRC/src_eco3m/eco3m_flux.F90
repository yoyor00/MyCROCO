                           ')%valpar(',  kk  ,')', ', & '
             enddo

             write(file_saveflx_id,*) '     FLUX_PAR(',  ichain2(:lon_ichain2)  ,&
                         ')%valpar(',  dim_param  ,') )'
           ! -----------------------------------------------------------------
           else !----  function without parameters ------
           ! -----------------------------------------------------------------
             dim_param=0
             signe_facteur =TRIM(ADJUSTL(FLUX_PAR(idproc_mat)%signe))

             write(file_saveflx_id,*) ' '
             write(file_saveflx_id,*) 'ss_flux(',idproc_loc,',:,:,:) =  &'

