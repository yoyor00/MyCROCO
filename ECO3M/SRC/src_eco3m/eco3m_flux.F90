               else
                  write(file_saveflx_id,'(a,a1,a6)') '(',FLUX_PAR(idproc_mat)%signe, '1) * &'
               endif
             endif

             write(file_saveflx_id,*)'  ',nomsub(1:len_trim(nomsub)),'(', &
                                    c_ili(:lon_c_ili),',',c_jcol(:lon_c_jcol),')'
            endif
