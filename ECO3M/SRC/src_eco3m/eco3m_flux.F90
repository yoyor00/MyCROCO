      ! the ili --> jcol total flux is written in the last column of the ss_flux table:
             write(file_saveflx_id,"(A, I3, A, I3, A)") &
            "    ss_flux(nbproc_loc+1,:,:,:) = FLUX_VAL(", ili, ",", jcol, ")%val"
